# PLOT 3: Weak Scaling
# ============================================================
def plot_weak_scaling():
    data = read_csv("weak_scaling.csv")
    if not data:
        return

    threads = [int(r['threads']) for r in data]
    times = [float(r['time_ms']) for r in data]

    fig, ax = plt.subplots(figsize=(6, 4.5))
    ax.plot(threads, times, 'D-', linewidth=2, markersize=8, 
            color='#2E86AB', label='Execution Time')

    # Ideal line (constant time)
    if len(times) > 0:
        ax.axhline(y=times[0], color='#E94F37', linestyle='--', alpha=0.7, 
                   linewidth=1.5, label=f'Ideal Constant ({times[0]:.1f} ms)')

    ax.set_xlabel('Number of Threads', fontsize=12)
    ax.set_ylabel('Execution Time (ms)', fontsize=12)
    ax.set_title('Weak Scaling: Time vs Thread Count\n(Fixed Work per Thread)', 
                 fontsize=13, fontweight='bold')
    ax.legend(loc='upper left', frameon=True, fancybox=True)
    ax.grid(True, alpha=0.3, linestyle='--')

    save_fig("fig3_weak_scaling.png")

# ============================================================
# PLOT 4: Execution Time Breakdown (Stacked Bar)
# ============================================================
def plot_execution_breakdown():
    data = read_csv("comparison.csv")
    if not data:
        return

    versions = [r['version'] for r in data]
    labels = [v.replace('sequential', 'Sequential').replace('openmp', 'OpenMP').replace('opencl', 'OpenCL') for v in versions]

    leak = [float(r.get('leak_ms', 0)) for r in data]
    prop = [float(r.get('propagate_ms', 0)) for r in data]
    spike = [float(r.get('spike_ms', 0)) for r in data]

    x = np.arange(len(labels))
    width = 0.6

    fig, ax = plt.subplots(figsize=(7, 5))
    ax.bar(x, leak, width, label='Leak (Decay)', color='#A8DADC')
    ax.bar(x, prop, width, bottom=leak, label='Propagate (Spike Routing)', color='#E94F37')
    ax.bar(x, spike, width, bottom=[l+p for l,p in zip(leak, prop)], 
           label='Spike Check (Threshold)', color='#2E86AB')

    ax.set_ylabel('Execution Time (ms)', fontsize=12)
    ax.set_title('Execution Time Breakdown by Phase', fontsize=13, fontweight='bold')
    ax.set_xticks(x)
    ax.set_xticklabels(labels)
    ax.legend(loc='upper right', frameon=True, fancybox=True)
    ax.grid(True, alpha=0.3, linestyle='--', axis='y')

    save_fig("fig4_execution_breakdown.png")

# ============================================================
# PLOT 5: SOPS Comparison
# ============================================================
def plot_sops_comparison():
    data = read_csv("comparison.csv")
    if not data:
        return

    versions = [r['version'] for r in data]
    labels = [v.replace('sequential', 'Sequential').replace('openmp', 'OpenMP').replace('opencl', 'OpenCL') for v in versions]

    # Parse SOPS (handle scientific notation strings)
    sops_vals = []
    for r in data:
        sops_str = r.get('sops', '0').replace('e+', 'e').replace('e-', 'e-')
        try:
            sops_vals.append(float(sops_str))
        except:
            sops_vals.append(0)

    x = np.arange(len(labels))
    colors = ['#A8DADC', '#2E86AB', '#E94F37']

    fig, ax = plt.subplots(figsize=(7, 5))
    bars = ax.bar(x, [s/1e9 for s in sops_vals], 0.5, color=colors[:len(labels)])

    ax.set_ylabel('Throughput (Giga-SOPS)', fontsize=12)
    ax.set_title('Synaptic Operations Per Second (SOPS)', fontsize=13, fontweight='bold')
    ax.set_xticks(x)
    ax.set_xticklabels(labels)
    ax.grid(True, alpha=0.3, linestyle='--', axis='y')

    # Add value labels on bars
    for bar, val in zip(bars, sops_vals):
        height = bar.get_height()
        ax.text(bar.get_x() + bar.get_width()/2., height,
                f'{val:.2e}',
                ha='center', va='bottom', fontsize=9)

    save_fig("fig5_sops_comparison.png")

# ============================================================
# PLOT 6: Speedup Comparison (Bar Chart)
# ============================================================
def plot_speedup_comparison():
    data = read_csv("comparison.csv")
    if not data:
        return

    versions = [r['version'] for r in data]
    labels = [v.replace('sequential', 'Sequential').replace('openmp', 'OpenMP').replace('opencl', 'OpenCL') for v in versions]
    speedups = [float(r.get('speedup_vs_seq', 1)) for r in data]

    x = np.arange(len(labels))
    colors = ['#A8DADC', '#2E86AB', '#E94F37']

    fig, ax = plt.subplots(figsize=(7, 5))
    bars = ax.bar(x, speedups, 0.5, color=colors[:len(labels)])

    ax.set_ylabel('Speedup vs Sequential', fontsize=12)
    ax.set_title('Overall Speedup Comparison', fontsize=13, fontweight='bold')
    ax.set_xticks(x)
    ax.set_xticklabels(labels)
    ax.grid(True, alpha=0.3, linestyle='--', axis='y')
    ax.axhline(y=1, color='gray', linestyle='--', alpha=0.5)

    for bar, val in zip(bars, speedups):
        height = bar.get_height()
        ax.text(bar.get_x() + bar.get_width()/2., height,
                f'{val:.2f}x',
                ha='center', va='bottom', fontsize=10, fontweight='bold')

    save_fig("fig6_speedup_comparison.png")

# ============================================================
# PLOT 7: Work-Group Size Sweep (OpenCL)
# ============================================================
def plot_wg_sweep():
    data = read_csv("wg_sweep.csv")
    if not data:
        return

    wg_sizes = [int(r['wg_size']) for r in data]
    times = [float(r['total_time_ms']) for r in data]

    fig, ax = plt.subplots(figsize=(6, 4.5))
    ax.plot(wg_sizes, times, 'o-', linewidth=2, markersize=8, color='#2E86AB')

    # Mark minimum
    min_idx = times.index(min(times))
    ax.plot(wg_sizes[min_idx], times[min_idx], 'r*', markersize=15, 
            label=f'Optimal: wg={wg_sizes[min_idx]}')

    ax.set_xlabel('Work-Group Size', fontsize=12)
    ax.set_ylabel('Execution Time (ms)', fontsize=12)
    ax.set_title('OpenCL Work-Group Size Tuning', fontsize=13, fontweight='bold')
    ax.legend(loc='upper right', frameon=True, fancybox=True)
    ax.grid(True, alpha=0.3, linestyle='--')

    save_fig("fig7_wg_size_sweep.png")

