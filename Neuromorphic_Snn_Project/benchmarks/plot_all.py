#!/usr/bin/env python3
"""
plot_all.py

Generates all figures needed for the IEEE paper from benchmark CSV files.

Usage:
    python benchmarks/plot_all.py

Input:  results/*.csv files (generated by run_all_experiments.py)
Output: results/*.png files (ready for paper insertion)
"""

import matplotlib.pyplot as plt
import matplotlib
import numpy as np
import csv
import os

# Use a clean style suitable for academic papers
matplotlib.rcParams['font.family'] = 'serif'
matplotlib.rcParams['font.size'] = 11
matplotlib.rcParams['figure.dpi'] = 300

RESULTS_DIR = "results"
os.makedirs(RESULTS_DIR, exist_ok=True)

def read_csv(filename):
    """Read CSV and return list of dicts."""
    path = os.path.join(RESULTS_DIR, filename)
    if not os.path.exists(path):
        print(f"WARNING: {path} not found, skipping plot.")
        return []
    with open(path) as f:
        reader = csv.DictReader(f)
        return list(reader)

def save_fig(name):
    """Save figure with tight layout."""
    path = os.path.join(RESULTS_DIR, name)
    plt.tight_layout()
    plt.savefig(path, dpi=300, bbox_inches='tight')
    print(f"Saved: {path}")
    plt.close()

# ============================================================
# PLOT 1: Strong Scaling - Speedup
# ============================================================
def plot_strong_scaling_speedup():
    data = read_csv("strong_scaling.csv")
    if not data:
        return

    threads = [int(r['threads']) for r in data]
    speedups = [float(r['speedup']) for r in data]

    fig, ax = plt.subplots(figsize=(6, 4.5))
    ax.plot(threads, speedups, 'o-', linewidth=2, markersize=8, 
            color='#2E86AB', label='Measured Speedup')
    ax.plot(threads, threads, '--', color='#E94F37', alpha=0.7, 
            linewidth=1.5, label='Ideal Linear Speedup')

    ax.set_xlabel('Number of Threads', fontsize=12)
    ax.set_ylabel('Speedup ($T_{seq} / T_{par}$)', fontsize=12)
    ax.set_title('Strong Scaling: Speedup vs Thread Count', fontsize=13, fontweight='bold')
    ax.legend(loc='upper left', frameon=True, fancybox=True)
    ax.grid(True, alpha=0.3, linestyle='--')
    ax.set_xlim(0, max(threads) * 1.1)
    ax.set_ylim(0, max(max(threads), max(speedups)) * 1.1)

    save_fig("fig1_strong_scaling_speedup.png")

# ============================================================
# PLOT 2: Strong Scaling - Efficiency
# ============================================================
def plot_strong_scaling_efficiency():
    data = read_csv("strong_scaling.csv")
    if not data:
        return

    threads = [int(r['threads']) for r in data]
    efficiencies = [float(r['efficiency']) for r in data]

    fig, ax = plt.subplots(figsize=(6, 4.5))
